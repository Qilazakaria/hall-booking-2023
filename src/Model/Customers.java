package Model;

public class Customers {
	
	private int custid;
	private String custidentificationcard;
	private String custname;
	private String custtelnum;
	private int custhomeno;
	private String custaddress;
	private String custcity;
	private int custpostcode;
	private String custstate;
	private String custemail;
	private String custpass;
	
	
	public int getCustid() {
		return custid;
	}
	public void setCustid(int custid) {
		this.custid = custid;
	}
	public String getCustidentificationcard() {
		return custidentificationcard;
	}
	public void setCustidentificationcard(String custidentificationcard) {
		this.custidentificationcard = custidentificationcard;
	}
	public String getCustname() {
		return custname;
	}
	public void setCustname(String custname) {
		this.custname = custname;
	}
	public String getCusttelnum() {
		return custtelnum;
	}
	public void setCusttelnum(String custtelnum) {
		this.custtelnum = custtelnum;
	}
	public int getCusthomeno() {
		return custhomeno;
	}
	public void setCusthomeno(int custhomeno) {
		this.custhomeno = custhomeno;
	}
	public String getCustaddress() {
		return custaddress;
	}
	public void setCustaddress(String custaddress) {
		this.custaddress = custaddress;
	}
	public String getCustcity() {
		return custcity;
	}
	public void setCustcity(String custcity) {
		this.custcity = custcity;
	}
	public int getCustpostcode() {
		return custpostcode;
	}
	public void setCustpostcode(int custpostcode) {
		this.custpostcode = custpostcode;
	}
	public String getCuststate() {
		return custstate;
	}
	public void setCuststate(String custstate) {
		this.custstate = custstate;
	}
	public String getCustemail() {
		return custemail;
	}
	public void setCustemail(String custemail) {
		this.custemail = custemail;
	}
	public String getCustpass() {
		return custpass;
	}
	public void setCustpass(String custpass) {
		this.custpass = custpass;
	}

}
