package Model;

public class Assets {
	
	private int assetsid;
	private String assetsname;
	private int assetsquantity;
	private String assetscategory;
	private String furnmaterial;
	private String electvoltage;
	private String voltageunit;
	
	public int getAssetsid() {
		return assetsid;
	}
	public void setAssetsid(int assetsid) {
		this.assetsid = assetsid;
	}
	public String getAssetsname() {
		return assetsname;
	}
	public void setAssetsname(String assetsname) {
		this.assetsname = assetsname;
	}
	public int getAssetsquantity() {
		return assetsquantity;
	}
	public void setAssetsquantity(int assetsquantity) {
		this.assetsquantity = assetsquantity;
	}
	public String getAssetscategory() {
		return assetscategory;
	}
	public void setAssetscategory(String assetscategory) {
		this.assetscategory = assetscategory;
	}
	public String getFurnmaterial() {
		return furnmaterial;
	}
	public void setFurnmaterial(String furnmaterial) {
		this.furnmaterial = furnmaterial;
	}
	public String getElectvoltage() {
		return electvoltage;
	}
	public void setElectvoltage(String electvoltage) {
		this.electvoltage = electvoltage;
	}
	public String getVoltageunit() {
		return voltageunit;
	}
	public void setVoltageunit(String voltageunit) {
		this.voltageunit = voltageunit;
	}
	
	

}
